def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):
    if y == 0:
        return "Error: Division by zero"
    return x / y

def exponentiation(x, y):
    return x ** y

def square_root(x):
    return x ** 0.5

def modulus(x, y):
    return x % y
